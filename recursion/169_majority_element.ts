function realCal(nums: number[], low: number, high: number): number {
    if (low == high) return nums[low];

    const mid = Math.floor((low + high) / 2);
    const left = realCal(nums, low, mid);
    const right = realCal(nums, mid + 1, high);

    if (left == right) return left;

    const leftCount = count(nums, left, low, high);
    const rightCount = count(nums, right, low, high);

    return leftCount > rightCount ? left : right;
}

function count(nums: number[], target: number, low: number, high: number): number {
    let count = 0;
    for (let i = low; i <= high; i++) {
        if (nums[i] == target) count++;
    }

    return count;
}

